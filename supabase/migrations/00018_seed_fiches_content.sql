Le trouble du spectre de l''autisme (TSA) est un trouble neurodéveloppemental qui modifie la façon dont une personne perçoit son environnement, communique et interagit avec les autres. Il s''agit d''un "spectre" : chaque enfant présente un profil unique, avec ses propres forces et difficultés.

