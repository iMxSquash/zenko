update public.fiches set content = '## Comprendre la crise

Une crise (ou "meltdown") n''est pas un caprice : c''est une réaction de débordement face à une accumulation de stress, de stimulations sensorielles ou de frustrations que l''enfant ne parvient plus à gérer.

## Avant la crise : anticiper

- Repérer les signes avant-coureurs : agitation, repli, bruits répétitifs, mains sur les oreilles.
- Identifier les déclencheurs récurrents (bruit, changement imprévu, fatigue, faim).
- Prévoir un "coin calme" où l''enfant peut se retirer pour se ressourcer.
- Utiliser un objet ou un casque anti-bruit comme outil d''apaisement disponible à tout moment.

## Pendant la crise : apaiser

- Rester calme et parler doucement, sans hausser le ton.
- Réduire les stimulations : baisser la lumière, éloigner le bruit, dégager l''espace autour de l''enfant.
- Ne pas forcer le contact physique ni le regard si l''enfant ne le recherche pas.
- Attendre que la tempête émotionnelle redescende avant de chercher à raisonner ou à expliquer.

## Après la crise

- Proposer un retour au calme progressif (boisson, activité sensorielle apaisante).
- Reparler de l''événement plus tard, avec des mots simples, pour identifier ensemble ce qui pourrait aider la prochaine fois.
- Éviter les punitions : la crise est une réaction de stress, pas un comportement volontaire.'
