update public.fiches set content = '## Comprendre la dyslexie

La dyslexie est un trouble spécifique de l''apprentissage qui affecte la reconnaissance des mots écrits, la fluidité et parfois la compréhension de la lecture. Ce n''est ni un problème de vue, ni un manque d''intelligence ou de travail.

## Adapter les supports de lecture

- Utiliser une police de caractères adaptée (sans empattement, interlignage augmenté).
- Privilégier des textes aérés, avec des paragraphes courts et une mise en page claire.
- Proposer des supports en couleur ou des règles de lecture (cache-ligne) pour limiter la perte de repères dans le texte.
- Donner accès à des versions audio des textes (livres audio, synthèse vocale) en complément de la lecture.

## Accompagner sans décourager

- Encourager la lecture à voix haute dans un cadre rassurant, sans corriger chaque erreur immédiatement.
- Valoriser la compréhension globale du texte plutôt que la performance de déchiffrage.
- Proposer des temps de lecture courts et réguliers plutôt que de longues sessions.
- Choisir des sujets qui intéressent vraiment l''enfant pour maintenir sa motivation.

## À l''école

Les aménagements comme le tiers temps, les supports agrandis ou l''autorisation d''utiliser un ordinateur peuvent faire une réelle différence dans la réussite scolaire et la confiance en soi de l''enfant.'
