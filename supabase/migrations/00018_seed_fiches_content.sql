- Mettre en place des routines visuelles (planning de la journée illustré).
- Préparer l''enfant aux changements (visite d''un nouveau lieu en amont, photos, explications simples).
- Observer les signaux de surcharge sensorielle pour proposer une pause avant que la situation ne devienne difficile.
- Valoriser les réussites, même petites, pour renforcer la confiance en soi.'
