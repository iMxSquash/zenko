update public.fiches set content = '## Pourquoi les routines aident un enfant TDAH

Un enfant avec un trouble du déficit de l''attention avec ou sans hyperactivité (TDAH) a souvent du mal à s''organiser seul, à enchaîner les étapes d''une tâche ou à gérer le temps. Une routine claire et stable réduit la charge mentale et limite les conflits liés aux oublis ou aux retards.

## Mettre en place une routine efficace

- **Découper la journée en blocs visuels** : matin, retour de l''école, devoirs, repas, coucher, avec des pictogrammes ou une frise horaire affichée.
- **Garder les mêmes horaires** autant que possible, y compris le week-end, pour ancrer les repères.
- **Limiter le nombre d''étapes** par séquence (3 à 5 maximum) pour ne pas surcharger.
- **Utiliser des supports concrets** : minuteur visuel, sablier, applications de gestion du temps adaptées aux enfants.

## Encourager l''autonomie

- Laisser l''enfant cocher lui-même les étapes réalisées sur un tableau.
- Prévoir des transitions annoncées ("dans 5 minutes, on range les jouets") pour éviter les changements brusques.
- Valoriser les efforts d''organisation, même imparfaits, plutôt que de se concentrer uniquement sur le résultat.

## En cas de difficulté

Si la routine n''est pas suivie, éviter de la complexifier davantage : il vaut souvent mieux la simplifier et la reconstruire progressivement, étape par étape.'
