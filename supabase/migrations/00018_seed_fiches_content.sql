update public.fiches set content = '## Le défi des devoirs avec un enfant TDAH

Les devoirs représentent souvent un moment de tension : difficulté à démarrer, à rester concentré, à organiser le matériel ou à estimer le temps nécessaire. Quelques outils simples peuvent considérablement alléger cette charge.

## Avant de commencer

- Choisir un moment fixe, idéalement après une pause active (jeu, mouvement) plutôt que juste après l''école.
- Aménager un espace de travail dégagé, avec uniquement le matériel nécessaire sous les yeux.
- Découper les devoirs en petites tâches listées, dans l''ordre à réaliser.

## Pendant le travail

- Utiliser un minuteur pour des sessions courtes (15 à 20 minutes) suivies de courtes pauses.
- Reformuler les consignes avec l''enfant pour vérifier qu''elles sont bien comprises.
- Cocher chaque tâche terminée sur une liste visuelle : voir sa progression est très motivant.

## Outils utiles

- Un agenda ou planning hebdomadaire visuel pour anticiper les échéances.
- Des codes couleur par matière pour retrouver rapidement les bons cahiers et classeurs.
- Une checklist du cartable à vérifier chaque soir, pour limiter le stress du "matériel oublié".

## Garder le lien avec l''école

Échanger régulièrement avec l''enseignant permet d''ajuster la quantité de travail et de mettre en place des aménagements (plus de temps, consignes simplifiées) si nécessaire.'
